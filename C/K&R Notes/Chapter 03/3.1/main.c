//Chapter 3: Control Flow

//3.1: Statements and Blocks

#include <stdio.h>

int main(void)
{
	//A *statement* is any expression that ends with a semicolon
	int x = 50;
	int y = 132;

	/* Braces are used to group multiple statements together, making them
	equivalent to a single statement. */

	printf("%d %d\n", x, y);

	return 0;
}
